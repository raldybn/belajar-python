#OPERASI ARITMATIKA

a = 8
b = 3
c = 2.5


#Penjumlahan
hasil = a + b
print(a,'+',b,'=',hasil)

#pengurangan
hasil = a - b
print(a,'-',b,'=',hasil)

#perkalian
hasil = a * b
print(a,'*',b,'=',hasil)

#pembagian
hasil = a / b
print(a,'/',b,'=',hasil)

#eksponen atau pangkat
hasil = a ** b
print(a,'**',b,'=',hasil)

#floor division atau pembagian hasil dibulatkan ke bawah
hasil = a // b
print(a,'//',b,'=',hasil)

#prioritas operasi aritmatika
hasil = a + b * c
print(a,'+',b,'*', c,'=',hasil)

#prioritas operasi aritmatika dengan tanda kurung
hasil = (a + b) * c
print('(',a,'+',b,') *', c,'=',hasil)

#operasi aritmatika dengan tipe data campuran

hasil = a + c
print(a,'+',c,'=',hasil)

#modulus atau sisa dari pembagian
hasil = a % b
print(a,'%',b,'=',hasil)

#operasi floor division (KEBALIKAN DARI MODULUS) atau hasil dari pembagian yang dibulatkan
hasil = a // b
print(a,'//',b,'=',hasil)
